type GenerateCodeRequest struct {
	Node     model.MibNode `json:"node"`
	Language string        `json:"language"` // python, javascript, go, java, shell
}

func (s *GeneratorService) GenerateCode(req *GenerateCodeRequest) string {
	switch req.Language {
	case "python":
		return s.generatePython(&req.Node)
	case "javascript":
		return s.generateJavaScript(&req.Node)
	case "go":
		return s.generateGo(&req.Node)
	case "java":
		return s.generateJava(&req.Node)
	case "shell":
		return s.generateShell(&req.Node)
	default:
		return fmt.Sprintf("// Unsupported language: %s", req.Language)
	}
}

func (s *GeneratorService) generatePython(node *model.MibNode) string {
	return fmt.Sprintf(`#!/usr/bin/env python3
"""
SNMP Query for: %s
OID: %s
Generated by SNMP MIB Explorer
"""

from pysnmp.hlapi import *

def snmp_get(host, community, oid):
    """Perform SNMP GET operation"""
    iterator = getCmd(
        SnmpEngine(),
        CommunityData(community),
        UdpTransportTarget((host, 161), timeout=10, retries=3),
        ContextData(),
        ObjectType(ObjectIdentity(oid))
    )

    errorIndication, errorStatus, errorIndex, varBinds = next(iterator)

    if errorIndication:
        print(f"Error: {errorIndication}")
        return None
    elif errorStatus:
        print(f"Error: {errorStatus.prettyPrint()} at {errorIndex}")
        return None
    else:
        for varBind in varBinds:
            return varBind[1].prettyPrint()
    return None

def snmp_walk(host, community, oid):
    """Perform SNMP WALK operation"""
    results = []
    for (errorIndication, errorStatus, errorIndex, varBinds) in nextCmd(
        SnmpEngine(),
        CommunityData(community),
        UdpTransportTarget((host, 161), timeout=10, retries=3),
        ContextData(),
        ObjectType(ObjectIdentity(oid)),
        lexicographicMode=False
    ):
        if errorIndication:
            print(f"Error: {errorIndication}")
            break
        elif errorStatus:
            print(f"Error: {errorStatus.prettyPrint()}")
            break
        else:
            for varBind in varBinds:
                results.append({
                    'oid': varBind[0].prettyPrint(),
                    'value': varBind[1].prettyPrint()
                })
    return results

if __name__ == "__main__":
    HOST = "127.0.0.1"
    COMMUNITY = "public"
    OID = "%s"

    print(f"Querying {OID} ({node_name})...")
    result = snmp_get(HOST, COMMUNITY, OID)
    print(f"Result: {result}")
`, node.Name, node.OID, node.OID)
}

func (s *GeneratorService) generateJavaScript(node *model.MibNode) string {
	return fmt.Sprintf(`/**
 * SNMP Query for: %s
 * OID: %s
 * Generated by SNMP MIB Explorer
 */

const snmp = require("net-snmp");

const HOST = "127.0.0.1";
const COMMUNITY = "public";
const OID = "%s";

// Create SNMP session
const session = snmp.createSession(HOST, COMMUNITY, {
  port: 161,
  timeout: 10000,
  retries: 3,
  version: snmp.Version2c
});

// SNMP GET
function snmpGet(oid) {
  return new Promise((resolve, reject) => {
    session.get([oid], (error, varbinds) => {
      if (error) {
        reject(error);
      } else {
        const results = varbinds.map(vb => ({
          oid: vb.oid,
          type: snmp.ObjectType[vb.type],
          value: vb.value.toString()
        }));
        resolve(results);
      }
    });
  });
}

// SNMP WALK
function snmpWalk(oid) {
  return new Promise((resolve, reject) => {
    const results = [];
    session.walk(oid, 20, (varbinds) => {
      varbinds.forEach(vb => {
        results.push({
          oid: vb.oid,
          type: snmp.ObjectType[vb.type],
          value: vb.value.toString()
        });
      });
    }, (error) => {
      if (error) {
        reject(error);
      } else {
        resolve(results);
      }
    });
  });
}

// Main execution
async function main() {
  try {
    console.log("Querying " + OID + " (%s)...");
    const results = await snmpGet(OID);
    console.log("Results:", JSON.stringify(results, null, 2));
  } catch (err) {
    console.error("Error:", err.message);
  } finally {
    session.close();
  }
}

main();
`, node.Name, node.OID, node.OID, node.Name)
}

func (s *GeneratorService) generateGo(node *model.MibNode) string {
	return fmt.Sprintf(`package main

/*
 * SNMP Query for: %s
 * OID: %s
 * Generated by SNMP MIB Explorer
 */

import (
	"fmt"
	"log"
	"time"

	"github.com/gosnmp/gosnmp"
)

func main() {
	// Configure SNMP parameters
	params := &gosnmp.GoSNMP{
		Target:    "127.0.0.1",
		Port:      161,
		Community: "public",
		Version:   gosnmp.Version2c,
		Timeout:   time.Duration(10) * time.Second,
		Retries:   3,
	}

	// Connect
	err := params.Connect()
	if err != nil {
		log.Fatalf("Connect error: %%v", err)
	}
	defer params.Conn.Close()

	// Define OID to query
	oid := "%s"
	oids := []string{oid}

	// Perform SNMP GET
	fmt.Printf("Querying %%s (%s)...\n", oid)
	result, err := params.Get(oids)
	if err != nil {
		log.Fatalf("Get error: %%v", err)
	}

	// Process results
	for _, variable := range result.Variables {
		fmt.Printf("OID: %%s\n", variable.Name)
		fmt.Printf("Type: %%s\n", getTypeName(variable.Type))
		fmt.Printf("Value: %%v\n", variable.Value)
	}
}

// SNMP Walk example
func walkExample(params *gosnmp.GoSNMP, rootOid string) {
	fmt.Printf("\nWalking %%s...\n", rootOid)

	err := params.Walk(rootOid, func(pdu gosnmp.SnmpPDU) error {
		fmt.Printf("%%s = %%v\n", pdu.Name, pdu.Value)
		return nil
	})

	if err != nil {
		log.Printf("Walk error: %%v", err)
	}
}

func getTypeName(t gosnmp.Asn1BER) string {
	names := map[gosnmp.Asn1BER]string{
		gosnmp.Integer:          "Integer",
		gosnmp.OctetString:      "OctetString",
		gosnmp.Null:             "Null",
		gosnmp.ObjectIdentifier: "OID",
		gosnmp.Counter32:        "Counter32",
		gosnmp.Gauge32:          "Gauge32",
		gosnmp.TimeTicks:        "TimeTicks",
		gosnmp.Counter64:        "Counter64",
		gosnmp.IPAddress:        "IPAddress",
	}
	if name, ok := names[t]; ok {
		return name
	}
	return fmt.Sprintf("Unknown(%%d)", t)
}
`, node.Name, node.OID, node.OID, node.Name)
}

func (s *GeneratorService) generateJava(node *model.MibNode) string {
	return fmt.Sprintf(`/**
 * SNMP Query for: %s
 * OID: %s
 * Generated by SNMP MIB Explorer
 *
 * Dependencies: snmp4j library
 * Maven: <dependency><groupId>org.snmp4j</groupId><artifactId>snmp4j</artifactId><version>3.7.0</version></dependency>
 */

import org.snmp4j.*;
import org.snmp4j.event.ResponseEvent;
import org.snmp4j.mp.SnmpConstants;
import org.snmp4j.smi.*;
import org.snmp4j.transport.DefaultUdpTransportMapping;

public class SnmpQuery {

    private static final String HOST = "127.0.0.1";
    private static final int PORT = 161;
    private static final String COMMUNITY = "public";
    private static final String OID = "%s";

    public static void main(String[] args) throws Exception {
        // Create transport
        TransportMapping<?> transport = new DefaultUdpTransportMapping();
        transport.listen();

        // Create SNMP session
        Snmp snmp = new Snmp(transport);

        // Create target
        CommunityTarget<Address> target = new CommunityTarget<>();
        target.setCommunity(new OctetString(COMMUNITY));
        target.setAddress(new UdpAddress(HOST + "/" + PORT));
        target.setRetries(3);
        target.setTimeout(10000);
        target.setVersion(SnmpConstants.version2c);

        // Create PDU
        PDU pdu = new PDU();
        pdu.add(new VariableBinding(new OID(OID)));
        pdu.setType(PDU.GET);

        // Send request
        System.out.println("Querying " + OID + " (%s)...");
        ResponseEvent<?> response = snmp.send(pdu, target);

        // Process response
        if (response.getResponse() != null) {
            for (VariableBinding vb : response.getResponse().getVariableBindings()) {
                System.out.println("OID: " + vb.getOid());
                System.out.println("Value: " + vb.getVariable());
            }
        } else {
            System.out.println("No response received");
        }

        snmp.close();
    }
}
`, node.Name, node.OID, node.OID, node.Name)
}

