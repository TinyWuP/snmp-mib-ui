type GeneratorService struct{}

func NewGeneratorService() *GeneratorService {
	return &GeneratorService{}
}

// DeviceInfo represents a device for config generation
type DeviceInfo struct {
	Name      string `json:"name"`
	IP        string `json:"ip"`
	Port      int    `json:"port"`
	Community string `json:"community"`
	Version   string `json:"version"`
}

type GenerateConfigRequest struct {
	Collector string           `json:"collector"` // snmp-exporter, telegraf, categraf
	Version   string           `json:"version"`
	Nodes     []model.MibNode  `json:"nodes"`
	Community string           `json:"community"`
	MibRoot   string           `json:"mibRoot"`
	Devices   []DeviceInfo     `json:"devices"` // Selected devices for telegraf/categraf
}

func (s *GeneratorService) GenerateConfig(req *GenerateConfigRequest) string {
	timestamp := time.Now().Format("2006-01-02 15:04:05")

	switch req.Collector {
	case "snmp-exporter":
		return s.generateSnmpExporter(req, timestamp)
	case "telegraf":
		return s.generateTelegraf(req, timestamp)
	case "categraf":
		return s.generateCategraf(req, timestamp)
	default:
		return fmt.Sprintf("# Unknown collector type: %s", req.Collector)
	}
}

func (s *GeneratorService) generateSnmpExporter(req *GenerateConfigRequest, timestamp string) string {
	community := req.Community
	if community == "" {
		community = "public"
	}

	var sb strings.Builder
	sb.WriteString(fmt.Sprintf("# SNMP Exporter Configuration\n"))
	sb.WriteString(fmt.Sprintf("# Generated by SNMP MIB Explorer\n"))
	sb.WriteString(fmt.Sprintf("# Version: %s\n", req.Version))
	sb.WriteString(fmt.Sprintf("# Date: %s\n", timestamp))
	sb.WriteString(fmt.Sprintf("# MIB Root: %s\n", req.MibRoot))
	sb.WriteString(fmt.Sprintf("# Metrics Count: %d\n\n", len(req.Nodes)))

	sb.WriteString("modules:\n")
	sb.WriteString("  default:\n")
	sb.WriteString("    walk:\n")

	for _, node := range req.Nodes {
		sb.WriteString(fmt.Sprintf("      - %s  # %s\n", node.OID, node.Name))
	}

	sb.WriteString("\n    metrics:\n")
	for _, node := range req.Nodes {
		safeName := sanitizeName(node.Name)
		help := node.Description
		if help == "" {
			help = node.Name
		}
		// Escape quotes in help text
		help = strings.ReplaceAll(help, `"`, `\"`)

		sb.WriteString(fmt.Sprintf("      - name: %s\n", safeName))
		sb.WriteString(fmt.Sprintf("        oid: %s\n", node.OID))
		sb.WriteString(fmt.Sprintf("        type: gauge\n"))
		sb.WriteString(fmt.Sprintf("        help: \"%s\"\n", help))
	}

	sb.WriteString("\n    auth:\n")
	sb.WriteString("      community: " + community + "\n")

	return sb.String()
}

func (s *GeneratorService) generateTelegraf(req *GenerateConfigRequest, timestamp string) string {
	community := req.Community
	if community == "" {
		community = "public"
	}

	// Build agents list from devices
	var agents []string
	if len(req.Devices) > 0 {
		for _, d := range req.Devices {
			port := d.Port
			if port == 0 {
				port = 161
			}
			agents = append(agents, fmt.Sprintf("\"udp://%s:%d\"", d.IP, port))
		}
		// Use first device's community if available
		if req.Devices[0].Community != "" {
			community = req.Devices[0].Community
		}
	} else {
		agents = []string{"\"udp://127.0.0.1:161\""}
	}

	var sb strings.Builder
	sb.WriteString("# Telegraf SNMP Input Configuration\n")
	sb.WriteString(fmt.Sprintf("# Generated by SNMP MIB Explorer\n"))
	sb.WriteString(fmt.Sprintf("# Version: %s\n", req.Version))
	sb.WriteString(fmt.Sprintf("# Date: %s\n", timestamp))
	sb.WriteString(fmt.Sprintf("# Devices: %d\n", len(req.Devices)))
	sb.WriteString(fmt.Sprintf("# Metrics Count: %d\n\n", len(req.Nodes)))

	sb.WriteString("[[inputs.snmp]]\n")
	sb.WriteString("  ## Agent addresses to retrieve values from.\n")
	sb.WriteString(fmt.Sprintf("  agents = [%s]\n\n", strings.Join(agents, ", ")))
	sb.WriteString("  ## Timeout for each request.\n")
	sb.WriteString("  timeout = \"10s\"\n\n")
	sb.WriteString("  ## SNMP version; can be 1, 2, or 3.\n")
	sb.WriteString("  version = 2\n\n")
	sb.WriteString("  ## SNMP community string.\n")
	sb.WriteString(fmt.Sprintf("  community = \"%s\"\n\n", community))
	sb.WriteString("  ## Measurement name\n")
	sb.WriteString("  name = \"snmp\"\n\n")
	sb.WriteString("  ## Add fields to represent OID values\n")

	for _, node := range req.Nodes {
		safeName := sanitizeName(node.Name)
		sb.WriteString(fmt.Sprintf("\n  [[inputs.snmp.field]]\n"))
		sb.WriteString(fmt.Sprintf("    name = \"%s\"\n", safeName))
		sb.WriteString(fmt.Sprintf("    oid = \"%s\"\n", node.OID))
		if node.Description != "" {
			sb.WriteString(fmt.Sprintf("    # %s\n", node.Description))
		}
	}

	sb.WriteString("\n  ## Add tables for table-based OIDs\n")
	sb.WriteString("  # [[inputs.snmp.table]]\n")
	sb.WriteString("  #   name = \"interface\"\n")
	sb.WriteString("  #   oid = \"IF-MIB::ifTable\"\n")

	return sb.String()
}

